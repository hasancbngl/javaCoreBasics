public class Programmer {

    String name;
    int id;
    double salary;

    Programmer() {
        name="john";
        id = 1;
        salary = 1000;
    }

    public Programmer(String name, int id, double salary) {
        this.name = name;
        this.id = id;
        this.salary = salary;
    }



}
